package com.infosys.enums.demos;

public class Pizza {
private int id;
private String name;
private String type;
private PizzaSize2 size;
/**
 * @return the id
 */
public int getId() {
	return id;
}
/**
 * @param id the id to set
 */
public void setId(int id) {
	this.id = id;
}
/**
 * @return the name
 */
public String getName() {
	return name;
}
/**
 * @param name the name to set
 */
public void setName(String name) {
	this.name = name;
}
/**
 * @return the type
 */
public String getType() {
	return type;
}
/**
 * @param type the type to set
 */
public void setType(String type) {
	this.type = type;
}
/**
 * @return the size
 */
public PizzaSize2 getSize() {
	return size;
}
/**
 * @param size the size to set
 */
public void setSize(PizzaSize2 size) {
	this.size = size;
}



}
